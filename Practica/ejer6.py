# Realizar un programa que permita ingresar por 
# teclado una cadena la misma que debe ser 
# invertida, mostrar por pantalla ambas cadenas.

cadena_original = input("Ingrese una cadena de texto: ")

cadena_invertida = cadena_original[::-1]

print("La cadena original es:", cadena_original)
print("La cadena invertida es:", cadena_invertida)
